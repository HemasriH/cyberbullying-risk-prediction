#cleaning the comment
import re
def cleantext(text):
  text=str(text).lower()
  text=re.sub(r'http\S+','',text)
  text = re.sub(r"[^a-z\s]", "", text)
  text = re.sub(r"\s+", " ", text)
  text = re.sub(r"[^\x00-\x7F]+", "", text)#ascii char
  return text.strip()

data1['cleaned_text']=data1['comment_text'].apply(cleantext)
data1[['comment_text','cleaned_text']].head()

s= SentimentIntensityAnalyzer()
def get_sentiment(text):
    score = s.polarity_scores(str(text))
    return score['compound']

data1['sentiment_score'] = data1['cleaned_text'].apply(get_sentiment)

data1[['cleaned_text', 'sentiment_score']].head()

def sentiment_label(score):
    if score >= 0.05:
        return "Positive"
    elif score <= -0.05:
        return "Negative"
    else:
        return "Neutral"

data1['sentiment_label'] = data1['sentiment_score'].apply(sentiment_label)

def risk_level(score):
    if score <= -0.5:
        return "Cyberbullying"
    elif score <= -0.1:
        return "Risky"
    else:
        return "Safe"

data1['risk_level'] = data1['sentiment_score'].apply(risk_level)
data1.head(50)

